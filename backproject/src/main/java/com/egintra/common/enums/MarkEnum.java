package com.egintra.common.enums;

/**
 * 是否标志枚举
 *
 * @author liushihao
 * @date 2021/6/10
 */
public enum MarkEnum {
    /**
     * 否
     */
    NO("0", "否"),

    /**
     * 是
     */
    YES("1", "是");

    private String code;
    private String desc;

    MarkEnum(String code, String desc) {
        this.code = code;
        this.desc = desc;
    }

    public String getCode() {
        return code;
    }

    public String getDesc() {
        return desc;
    }
}
