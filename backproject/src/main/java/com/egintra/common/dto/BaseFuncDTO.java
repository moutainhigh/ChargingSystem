package com.egintra.common.dto;

/**
 * 基础DTO：记录操作日志所需参数
 *
 * @author liushihao
 * @date 2021/7/08
 */
public class BaseFuncDTO {

    /**
     * 功能菜单ID
     */
    private String funcMenuId;

    /**
     * 分类菜单ID
     */
    private String cateMenuId;

    public String getFuncMenuId() {
        return funcMenuId;
    }

    public void setFuncMenuId(String funcMenuId) {
        this.funcMenuId = funcMenuId;
    }

    public String getCateMenuId() {
        return cateMenuId;
    }

    public void setCateMenuId(String cateMenuId) {
        this.cateMenuId = cateMenuId;
    }

    @Override
    public String toString() {
        return "BaseFuncDTO{" +
                "funcMenuId=" + funcMenuId +
                ", cateMenuId=" + cateMenuId +
                '}';
    }
}
