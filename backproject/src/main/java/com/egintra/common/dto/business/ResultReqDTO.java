package com.egintra.common.dto.business;

/**
 * 业务缴费页面需要的参数
 *
 * @author zhangyunting
 * @date 2021/9/8
 */
public class ResultReqDTO {

    /**
     * 收款人
     */
    private String payee;

    /**
     * 执行单位编码
     */
    private String  receiceAccount;

    /**
     * 账套时间
     */
    private String workDate;

    /**
     * noA
     */
    private String NoA;

    /**
     * 校验码
     */
    private String fictitiousId;


    public String getPayee() {
        return payee;
    }

    public void setPayee(String payee) {
        this.payee = payee;
    }

    public String getReceiceAccount() {
        return receiceAccount;
    }

    public void setReceiceAccount(String receiceAccount) {
        this.receiceAccount = receiceAccount;
    }

    public String getWorkDate() {
        return workDate;
    }

    public void setWorkDate(String workDate) {
        this.workDate = workDate;
    }

    public String getNoA() {
        return NoA;
    }

    public void setNoA(String noA) {
        NoA = noA;
    }

    public String getFictitiousId() {
        return fictitiousId;
    }

    public void setFictitiousId(String fictitiousId) {
        this.fictitiousId = fictitiousId;
    }

    @Override
    public String toString() {
        return "ResultReq{" +
                "payee='" + payee + '\'' +
                ", receiceAccount='" + receiceAccount + '\'' +
                ", workDate='" + workDate + '\'' +
                ", NoA='" + NoA + '\'' +
                ", fictitiousId='" + fictitiousId + '\'' +
                '}';
    }
}
